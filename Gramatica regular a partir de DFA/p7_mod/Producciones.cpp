////////////////////////////////////////////////////////////////////////////////

bool Produccion::Buscar_part(string e){
	int cont=0;
	for (int i=0;i<part_derech.size();i++){
		if(e==part_derech[i])
			cont ++;
    }
    if(cont>0)
    	return true;
    else 
    	return false;
}

